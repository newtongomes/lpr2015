package cap05;
public class ArraySemana {
    public static void main(String[] args) {
        String[] semana = {"Domingo","Segunda","Terça","Quarta","Quinta","Sexta","Sábado"};
        for(int i=0;i<semana.length;i++){
            System.out.println(semana[i]);
        }
        System.exit(0);
    }
}

