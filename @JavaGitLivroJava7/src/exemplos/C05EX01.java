package exemplos;
import java.io.*;

class C05EX01 {
  public static void main(String args[]) {

    System.out.println();
    System.out.println(Math.acos(-1));    //  3.141592653589793
    System.out.println(Math.asin(-1));    // -1.5707963267948966
    System.out.println(Math.atan(1));     //  0.7853981633974483                  
    System.out.println(Math.sin(1));      //  0.8414709848078965
    System.out.println(Math.tan(4));      //  1.1578212823495777
    System.out.println(Math.pow(2,3));    //  8.0
    System.out.println(Math.sqrt(9.0));   //  3.0
    System.out.println(Math.exp(1));      //  2.7182818284590455               
    System.out.println(Math.log(3));      //  1.0986122886681096               
    System.out.println(Math.abs(-9));     //  9
    System.out.println(Math.ceil(2.03));  //  3                  
    System.out.println(Math.floor(2.03)); //  2.0                  
    System.out.println(Math.max(10,3));   //  10 
    System.out.println(Math.min(10,3));   //  3 
    System.out.println(Math.rint(18.7));  //  19.0                
    System.out.println(Math.round(5.8));  //  6                

  }
}
